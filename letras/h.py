import turtle

raphael = turtle.Turtle()

def letra_h() -> None:
    raphael.penup()
    raphael.pensize(5)
    raphael.goto(-50, 0)
    raphael.pendown()
    raphael.left(90)
    raphael.forward(100)
    raphael.right(180)
    raphael.forward(50)
    raphael.left(90)
    raphael.forward(50)
    raphael.left(90)
    raphael.forward(50)
    raphael.left(180)
    raphael.forward(100)